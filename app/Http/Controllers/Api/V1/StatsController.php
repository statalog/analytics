    }

    /**
     * GET /api/v1/devices
     * Visitors split by device type, browser, and OS.
     */
    public function devices(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        [$from, $to] = $this->dateRange($request);
        $id = $site->site_id;

        return response()->json([
            'site_id'  => $id,
            'from'     => $from,
            'to'       => $to,
            'devices'  => $this->analytics->getDevices($id, $from, $to),
            'browsers' => $this->analytics->getBrowsers($id, $from, $to, $this->limit($request, 10)),
            'os'       => $this->analytics->getOperatingSystems($id, $from, $to, $this->limit($request, 10)),
        ]);
    }

    /**
     * GET /api/v1/events
     * Custom events with counts and properties breakdown.
     */
    public function events(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        [$from, $to] = $this->dateRange($request);

        return response()->json([
            'site_id' => $site->site_id,
            'from'    => $from,
            'to'      => $to,
            'data'    => $this->analytics->getCustomEvents($site->site_id, $from, $to),
        ]);
    }

    /**
     * GET /api/v1/campaigns
     * UTM campaign stats.
     */
    public function campaigns(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        [$from, $to] = $this->dateRange($request);


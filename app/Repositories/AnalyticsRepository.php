        return (int) ($rows[0]['cnt'] ?? 0);
    }

    public function getBounceRate(string $siteId, string $from, string $to): float
    {
        $rows = $this->queryWithCache(
            "bounce:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT
                countIf(pv_count = 1) as bounced,
                count() as total
            FROM (
                SELECT session_id, count() as pv_count
                FROM pageviews
                WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to
                GROUP BY session_id
            )",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to]
        );
        $total = (int) ($rows[0]['total'] ?? 0);
        if ($total === 0) {
            return 0.0;
        }
        return round(((int) ($rows[0]['bounced'] ?? 0) / $total) * 100, 1);
    }

    public function getAvgVisitDuration(string $siteId, string $from, string $to): int
    {
        $rows = $this->queryWithCache(
            "avgduration:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT avg(duration) as avg_dur FROM (
                SELECT session_id, greatest(dateDiff('second', min(timestamp), max(timestamp)), max(visit_duration)) as duration
                FROM pageviews
                WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to AND session_id != ''
                GROUP BY session_id
                HAVING duration > 0
            )",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to]
        );
        return (int) ($rows[0]['avg_dur'] ?? 0);
    }

    // --- Chart Data ---

    public function getVisitorsOverTime(string $siteId, string $from, string $to, string $timezone = 'UTC'): array
    {
        return $this->queryWithCache(
            "visitors_chart:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT
                date,
                sum(visitors) as visitors,
                sum(pageviews) as pageviews,
                count() as sessions,
                round(countIf(pv_count = 1) * 100.0 / count(), 1) as bounce,
                toInt32(avg(duration)) as duration
            FROM (
                SELECT
                    session_id,
                    toDate(min(timestamp), :tz) as date,
                    uniq(visitor_id) as visitors,
                    countIf(url != '') as pv_count,
                    countIf(url != '') as pageviews,
                    greatest(dateDiff('second', min(timestamp), max(timestamp)), max(visit_duration)) as duration
                FROM pageviews
                WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to
                GROUP BY session_id
            )
            GROUP BY date
            ORDER BY date",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to, 'tz' => $timezone]
        );
    }


    // --- New vs Returning ---

    public function getNewVsReturning(string $siteId, string $from, string $to): array
    {
        return $this->queryWithCache(
            "newret:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT
                if(is_new_visitor = 1, 'New', 'Returning') as segment,
                uniq(visitor_id) as visitors,
                uniq(session_id) as sessions,
                countIf(url != '') as pageviews,
                round(avg(duration)) as avg_duration
            FROM (
                SELECT
                    session_id,
                    visitor_id,
                    any(is_new_visitor) as is_new_visitor,
                    countIf(url != '') as pv_count,
                    greatest(dateDiff('second', min(timestamp), max(timestamp)), max(visit_duration)) as duration
                FROM pageviews
                WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to
                GROUP BY session_id, visitor_id
            )
            GROUP BY segment",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to]
        );
    }

    public function getNewVsReturningOverTime(string $siteId, string $from, string $to, string $timezone = 'UTC'): array
    {
        return $this->queryWithCache(
            "newret_chart:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT
                toDate(timestamp, :tz) as date,
                countIf(is_new_visitor = 1) as new_visitors,
                countIf(is_new_visitor = 0) as returning_visitors
            FROM pageviews
            WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to
            GROUP BY date

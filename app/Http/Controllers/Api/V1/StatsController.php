        ]);
    }

    /**
     * GET /api/v1/live
     * Visitors active in the last 30 minutes.
     */
    public function live(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        $minutes = min((int) $request->query('minutes', 30), 60);

        return response()->json([
            'site_id'  => $site->site_id,
            'minutes'  => $minutes,
            'visitors' => $this->analytics->getLiveVisitorCount($site->site_id, $minutes),
        ]);
    }
}

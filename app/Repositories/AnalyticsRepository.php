    // --- Live Stats ---

    public function getLiveVisitors(string $siteId, int $minutes = 30, ?string $hostname = null): array
    {
        $hf = $hostname ? " AND domain(url) = :hostname" : '';
        $params = ['site_id' => $siteId];
        if ($hostname) {
            $params['hostname'] = $hostname;
        }

        $sql = "SELECT
                toStartOfMinute(timestamp) as minute,
                uniq(visitor_id) as visitors
            FROM pageviews
            WHERE site_id = :site_id" . $this->sd() . " AND url != '' AND timestamp >= now() - INTERVAL {$minutes} MINUTE{$hf}
            GROUP BY minute
            ORDER BY minute";

        return $this->query($sql, $params);
    }


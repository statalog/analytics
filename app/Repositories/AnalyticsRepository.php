namespace App\Repositories;

use Illuminate\Support\Facades\Cache;
use Illuminate\Support\Facades\Http;
use Illuminate\Support\Facades\Log;

class AnalyticsRepository
{
    protected string $host;
    protected int $port;
    protected string $database;
    protected string $username;
    protected string $password;
    protected bool $excludeSubdomains = false;
    protected string $registeredDomain = '';
    protected string $hostnameFilter = '';
    protected bool $includeBots = false;
    protected bool $onlyBots = false;

    public function __construct()
    {
        $this->host     = (string) config('clickhouse.connection.host', '127.0.0.1');
        $this->port     = (int) config('clickhouse.connection.port', 8123);
        $this->database = (string) config('clickhouse.connection.options.database', 'default');
        $this->username = (string) config('clickhouse.connection.username', 'default');
        $this->password = (string) config('clickhouse.connection.password', '');
    }

    /**
     * Return a clone of the repository that filters out subdomain hits.
     * $domain is the registered domain (e.g. "example.com").
     */
    public function withoutSubdomains(string $domain): static
    {
        $clone = clone $this;
        $clone->excludeSubdomains = true;
        $clone->registeredDomain = ltrim(preg_replace('/^www\./i', '', strtolower($domain)), '');
        return $clone;
    }

    /** Filter to a specific hostname (overrides subdomain exclude filter). */
    public function forHostname(string $hostname): static
    {
        $clone = clone $this;
        $clone->hostnameFilter = strtolower(trim($hostname));
        return $clone;
    }

    /** Include bot traffic in results (default: excluded). */
    public function includeBots(): static
    {
        $clone = clone $this;
        $clone->includeBots = true;
        return $clone;
    }

    /** Return ONLY bot traffic in results. */
    public function onlyBots(): static
    {
        $clone = clone $this;
        $clone->includeBots = true;
        $clone->onlyBots = true;
        return $clone;
    }

    /** Returns the extra WHERE fragment for hostname/subdomain + bot filtering. */
    protected function sd(): string
    {
        $clause = '';

        if ($this->hostnameFilter !== '') {
            $h = addslashes($this->hostnameFilter);
            $clause .= " AND hostname = '{$h}'";
        } elseif ($this->excludeSubdomains && $this->registeredDomain !== '') {
            $d = addslashes($this->registeredDomain);
            $clause .= " AND (hostname = '{$d}' OR hostname = 'www.{$d}' OR hostname = '')";
        }

        if ($this->onlyBots) {
            $clause .= ' AND is_bot = 1';
        } elseif (!$this->includeBots) {
            $clause .= ' AND is_bot = 0';
        }

        return $clause;
    }

    /** Returns cache key suffix for hostname/subdomain + bot filtering. */
    protected function sdKey(): string
    {
        $suffix = '';
        if ($this->hostnameFilter !== '') {
            $suffix .= ':hn=' . $this->hostnameFilter;
        } elseif ($this->excludeSubdomains) {
            $suffix .= ':nosd';
        }
        if ($this->onlyBots) {
            $suffix .= ':obots';
        } elseif ($this->includeBots) {
            $suffix .= ':wbots';
        }
        return $suffix;
    }

    /** Returns distinct hostnames for a site in the given date range. */
    public function getDistinctHostnames(string $siteId, string $from, string $to): array
    {
        return $this->queryWithCache(
            "hostnames:{$siteId}:{$from}:{$to}", 300,
            "SELECT hostname, count() as hits FROM pageviews WHERE site_id = :site_id AND timestamp >= :from AND timestamp <= :to AND hostname != '' GROUP BY hostname ORDER BY hits DESC LIMIT 50",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to]
        );
    }

    protected function query(string $sql, array $params = []): array
    {
        foreach ($params as $key => $value) {
            $escaped = is_string($value) ? "'" . addslashes($value) . "'" : $value;
            $sql = str_replace(':' . $key, (string) $escaped, $sql);
        }

        try {
            $response = Http::withBasicAuth($this->username, $this->password)
                ->timeout(config('clickhouse.timeout', 10))
                ->withBody($sql . ' FORMAT JSON', 'text/plain')
                ->post("http://{$this->host}:{$this->port}/?database={$this->database}");
        } catch (\Throwable $e) {
            // ClickHouse unreachable (dev environment without it running, network blip, etc).
            // Log once per request and return empty — the admin area stays usable with zero data.
            Log::warning('ClickHouse query failed: ' . $e->getMessage());
            return [];
        }

        if ($response->failed()) {
            Log::warning('ClickHouse query returned non-2xx: ' . $response->status() . ' ' . $response->body());
            return [];
        }

        $data = $response->json();
        return $data['data'] ?? [];
    }

    protected function queryWithCache(string $key, int $seconds, string $sql, array $params = []): array
    {
        return Cache::remember($key, $seconds, function () use ($sql, $params) {
            return $this->query($sql, $params);
        });
    }

    // --- Dashboard Stats ---

    public function getUniqueVisitors(string $siteId, string $from, string $to): int
    {
        $rows = $this->queryWithCache(
            "visitors:{$siteId}:{$from}:{$to}" . $this->sdKey(), 300,
            "SELECT uniq(visitor_id) as cnt FROM pageviews WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to",
            ['site_id' => $siteId, 'from' => $from, 'to' => $to]
        );
        return (int) ($rows[0]['cnt'] ?? 0);
    }


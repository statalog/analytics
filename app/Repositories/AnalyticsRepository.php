    /**
     * Returns pageviews, unique visitors, and sessions for multiple sites in one ClickHouse query.
     * Returns array keyed by site_id.
     */
    public function getSiteStatsBulk(array $siteIds, string $from, string $to): array
    {
        if (empty($siteIds)) {
            return [];
        }

        $placeholders = implode(',', array_map(fn ($id) => "'" . addslashes($id) . "'", $siteIds));

        $rows = $this->query(
            "SELECT site_id, count() as pageviews, uniq(visitor_id) as visitors, uniq(session_id) as sessions
             FROM pageviews
             WHERE site_id IN ({$placeholders}) AND timestamp >= :from AND timestamp <= :to
             GROUP BY site_id",
            ['from' => $from, 'to' => $to]
        );

        $result = [];
        foreach ($rows as $row) {
            $result[$row['site_id']] = [
                'pageviews' => (int) $row['pageviews'],
                'visitors'  => (int) $row['visitors'],
                'sessions'  => (int) $row['sessions'],
            ];
        }
        return $result;
    }

    /**
     * Returns today/this_month/last_month hit counts for each site_id in one query.
     * Result: ['SA-XXX' => ['today' => N, 'this_month' => N, 'last_month' => N], ...]
     */
    public function getSiteHitCounts(array $siteIds): array
    {
        if (empty($siteIds)) {
            return [];
        }

        $ids = implode("','", array_map(fn ($id) => preg_replace('/[^A-Za-z0-9\-]/', '', $id), $siteIds));

        $rows = $this->query(
            "SELECT
                site_id,
                countIf(timestamp >= toStartOfDay(now())) as today,
                countIf(timestamp >= toStartOfMonth(now())) as this_month,
                countIf(timestamp >= toStartOfMonth(subtractMonths(now(), 1)) AND timestamp < toStartOfMonth(now())) as last_month
            FROM pageviews
            WHERE site_id IN ('{$ids}') AND url != ''
            GROUP BY site_id"
        );

        $result = array_fill_keys($siteIds, ['today' => 0, 'this_month' => 0, 'last_month' => 0]);
        foreach ($rows as $row) {
            $result[$row['site_id']] = [
                'today'      => (int) $row['today'],
                'this_month' => (int) $row['this_month'],
                'last_month' => (int) $row['last_month'],
            ];
        }
        return $result;
    }


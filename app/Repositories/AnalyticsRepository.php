    // --- Funnel ---

    public function getFunnelData(string $siteId, array $stepPaths, string $from, string $to): array
    {
        $results = [];
        foreach ($stepPaths as $i => $path) {
            if ($i === 0) {
                $rows = $this->query(
                    "SELECT uniq(visitor_id) as visitors FROM pageviews WHERE site_id = :site_id" . $this->sd() . " AND url LIKE :path AND timestamp >= :from AND timestamp <= :to",
                    ['site_id' => $siteId, 'path' => '%' . $path . '%', 'from' => $from, 'to' => $to]
                );
                $results[] = (int) ($rows[0]['visitors'] ?? 0);
            } else {
                $prevPaths = array_slice($stepPaths, 0, $i + 1);
                $conditions = [];
                foreach ($prevPaths as $p) {
                    $conditions[] = "countIf(url LIKE '%" . addslashes($p) . "%') > 0";
                }
                $having = implode(' AND ', $conditions);
                $rows = $this->query(
                    "SELECT count() as visitors FROM (
                        SELECT session_id
                        FROM pageviews
                        WHERE site_id = :site_id" . $this->sd() . " AND timestamp >= :from AND timestamp <= :to
                        GROUP BY session_id
                        HAVING {$having}
                    )",
                    ['site_id' => $siteId, 'from' => $from, 'to' => $to]
                );
                $results[] = (int) ($rows[0]['visitors'] ?? 0);
            }
        }
        return $results;
    }


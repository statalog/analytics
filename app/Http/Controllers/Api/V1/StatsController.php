            'to'      => $to,
            'data'    => $this->analytics->getReferrerDomainStats($site->site_id, $from, $to),
        ]);
    }

    /**
     * GET /api/v1/channels
     * Traffic by channel (Organic, Direct, Referral, Social, Email, Paid, AI).
     */
    public function channels(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        [$from, $to] = $this->dateRange($request);

        return response()->json([
            'site_id' => $site->site_id,
            'from'    => $from,
            'to'      => $to,
            'data'    => $this->analytics->getTrafficSources($site->site_id, $from, $to, $this->limit($request)),
        ]);
    }

    /**
     * GET /api/v1/locations
     * Visitors by country.
     */
    public function locations(Request $request): JsonResponse
    {
        $site = $this->resolveSite($request);
        if ($site instanceof JsonResponse) return $site;

        [$from, $to] = $this->dateRange($request);

